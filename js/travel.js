// Create Header Links
var parentPage 	   = "travel.html";
const urlParams    = new URLSearchParams(window.location.search);    
const levelAValue  = urlParams.get('levelA');  
const levelBValue  = urlParams.get('levelB');    

var level0Href 	= '<a href="' + parentPage + '">TRAVEL</a>';
var levelALink  = '<a href="' + parentPage + '?levelA=' + levelAValue + '">' + levelAValue +'</a>';
var levelBLink 	= '<a href="' + parentPage + '?levelA=' + levelAValue + '&levelB='+levelBValue+'">'+levelBValue+'</a>';

var headerOutput;
if (levelAValue != null && levelBValue != null){
    headerOutput = level0Href + " <-> " + levelALink + " <-> " + levelBLink;
}
else if(levelAValue != null){
    headerOutput = level0Href + " <-> " + levelALink;
}
else{
    headerOutput = level0Href;
}

const datas = Object.entries(hashMap);
if (!levelAValue || !levelBValue)  
{
    let output  = '';
    var countriesDisplayed = new Set()
    for(let key of datas) 
    {
        var Name = key[0];
        var Desc = key[1]["Desc"];
        var Imag = key[1]["Images"][0];
        var Country = key[1]["Country"];

        // Level 1. Show all Countries
        if (levelAValue == null)      
        {
            //Display a country on Main page only once
            if(countriesDisplayed.has(Country))
                continue;
            else
                countriesDisplayed.add(Country);

            output += `
            <a href="${parentPage}?levelA=${Country}" class="item">
                <div class="img-container">
                    <img src="${Imag}" alt="">
                </div>
                <span>${Country}</span>
            </a>
            `;
        }
        // Level 2: Show only a specific Country
        else if (levelAValue == Country)
		{
			output += `
			<a href="${parentPage}?levelA=${Country}&levelB=${Name}" class="item">
				<div class="img-container">
					<img src="${Imag}" alt="">
				</div>
				<span>${Name}</span>
			</a>
			`;
		}
    }

    // Create element
    const div     = document.createElement("div");
    div.className = 'generic-items';
    div.innerHTML = output;

    document.querySelector(".generic-leaf-container").insertAdjacentElement("beforeend", div);
}
else
{
	var found = Object.entries(hashMap).find(y => {
        return y[0] == levelBValue;
    });

	var Name = found[0];
	var Desc = found[1]["Desc"];
	var Images = found[1]["Images"];

	let output=  '';
	for (let Imag of Images) 
	{
		if (Imag.includes('.mp4'))
		{
			output += `   
			<a href="${Imag}" class='grid-item' data-fancybox="gallery">
				<video width="550" height="450" controls>
					<source src="${Imag}#t=3" type="video/mp4">
				</video>
			</a>
			`;
		}
		else
		{
			output += `
			<a href="${Imag}" class='grid-item' data-fancybox="gallery">
				<img src="${Imag}">
			</a>
			`;
		}
	}

    // Create and Inject new Div
    const parentDiv 	= document.createElement("div");
    parentDiv.className = 'grid';
    parentDiv.innerHTML = `
        <div class="grid-col grid-col--1"></div>
        <div class="grid-col grid-col--2"></div>
        ${output}
    `;
	
	document.querySelector(".generic-description").innerHTML = Desc;

	
	document.querySelector("#generic-container .generic-leaf-container").insertAdjacentElement("beforeend", parentDiv);
}

document.querySelector(".generic-header").innerHTML= headerOutput;